namespace Gruzd_5;

/// <summary>
/// Единый реестр определений для банковской системы.
/// Содержит методологические указания по расчету процентов и визуализации активов.
/// </summary>
public interface IBankDocumentation
{
    /// <summary>
    /// БАЗОВЫЙ АККАУНТ: Абстрактная модель финансового инструмента. 
    /// Служит фундаментом для построения специализированных типов счетов.
    /// </summary>
    void BankAccountDefinition();

    /// <summary>
    /// РАСЧЕТ ПРОЦЕНТОВ: Математическая модель изменения баланса.
    /// Для сбережений: капитализация (начисление прибыли).
    /// Для текущих счетов: операционное списание (стоимость обслуживания).
    /// </summary>
    void CalculateInterest();

    /// <summary>
    /// ОТОБРАЖЕНИЕ СОСТОЯНИЯ: Процедура аудита текущей ликвидности.
    /// Выводит актуальный остаток денежных средств с учетом специфики счета.
    /// </summary>
    void DisplayBalance();
}